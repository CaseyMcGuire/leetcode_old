def removeDuplicates(A):
    if A is None or not A:
        return 0

    if len(A) == 1:
        return 1

    index = 1
    if A[0] == A[1]:
        index += 1


    for i in range(index, len(A)):
        if A[i] != A[index - 1]:
            A[index] = A[i]
            if i + 1 < len(A) and A[i + 1] == A[i]:
                i += 1
                index += 1
                A[index] = A[i]
            index += 1
    return index

